public class ConversorUnidades {

    public static double converterMetrosParaCentimetros(double metros) {
        return metros * 100;
    }

    public static double converterQuilogramasParaGramas(double quilogramas) {
        return quilogramas * 1000;
    }

    public static int converterHorasParaMinutos(int horas) {
        return horas * 60;
    }
}
